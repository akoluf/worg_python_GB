# Задача 22: Даны два неупорядоченных набора целых чисел (может быть, с
# повторениями). Выдать без повторений в порядке возрастания все те числа, которые встречаются в обоих наборах.
# Пользователь вводит 2 числа. n - кол-во элементов первого множества. m - кол-во
# элементов второго множества. Затем пользователь вводит сами элементы множеств.

import random

def generate_numbers(n, m):
    # Генерируем случайные числа для двух списков
    numbers_list1 = random.sample(range(1, 101), n)
    numbers_list2 = random.sample(range(1, 101), m)

    # Находим числа, которые встречаются и в первом, и во втором списке
    common_numbers = sorted(list(set(numbers_list1) & set(numbers_list2)))

    return common_numbers

# Запрашиваем количество чисел для первого списка
n = int(input("Введите количество чисел для первого списка: "))

# Запрашиваем количество чисел для второго списка
m = int(input("Введите количество чисел для второго списка: "))

# Генерируем и выводим результат
result = generate_numbers(n, m)
print("Числа, встречающиеся в обоих наборах:", result)
